            <h3 class="h3">
                10-весельная катер «Бывалый»</h3>
            <img alt="10vesel_shlup_byvaly.jpg" src="/upload/medialibrary/7f8/6pfdictjmn9pxo8e7yab52issea7cgxh.jpg"
                 title="10vesel_shlup_byvaly.jpg" class="floating">
            <p>
                10-ти весельный катер «Бывалый» – мореходная двухмачтовая парусно-гребная шлюпка с деревянным корпусом,
                набранным вгладь. Катер попал в Ассоциацию из Кронштадта, где был списан за ветхостью. Состояние лодки
                было плачевным –был деформирован корпус – средняя часть корпуса была вогнута внутрь. Также было много
                других локальных неисправностей и разрушений.
            </p>
            <p>
                Были произведены реставрационные работы. Все, что было возможным исправить – исправили. В итоге
                остойчивость лодки стала лучше, чем была изначально, но маневренность ухудшилась. Сейчас шлюп активно
                используется для отработки навыков хождения под парусом в акватории Истринского водохранилища.
            </p>
            <p>
            </p>

