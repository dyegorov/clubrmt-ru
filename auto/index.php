            <h3 class="h3">
                14-весельный катер «Император» </h3>
            <img alt="14vesel_kater_imperator.jpg" src="/upload/medialibrary/887/eo4sjhy96kgf5ug66hw8srsb7fa6ya9r.jpg"
                 title="14vesel_kater_imperator.jpg" class="floating">
            <p>
                Десятиметровый 14-ти весельный катер – это трехмачтовый легкий катер с латинским парусным вооружением.
                Такие лодки чаще всего сопровождали большие военные корабли в императорском дореволюционном флоте. Был
                такой катер, например, на знаменитом крейсере 1 ранга «Аврора».
            </p>
            <p>
                Катер был построен в Петрозаводске на верфи деревянного судостроения «Варяг». В основе конструкции –
                чертежи 1915 года, предоставленные главным архивом ВМФ. Свой первый выход «Император» совершил в
                акватории Онежского озера в 2014 г.
            </p>
