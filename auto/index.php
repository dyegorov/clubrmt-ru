            <h1 class="h1">
                Военные автомобили </h1>
            <p class="subtitle">
                Ассоциация ведет планомерную работу по сохранению исторического наследия парусно-гребных шлюпов. Флот
                ассоциации, участвующий в регатах и походах, состоит из восстановленных деревянных парусно-гребных ЯЛов
                и катера ВМФ, а также реплик катеров, построенных по историческим чертежам. Собрание исторических
                деревянных парусно-гребных включает 3 катера, 1 вельбот и 3 шлюпки.
            </p>
            <h3 class="h3">
                16-весельный катер «Александр II» </h3>
            <p>
                26 Декабря 2022 года на верфи деревянного кораблестроения «Варяг» в Петрозаводске по проекту и при
                поддержке Ассоциации «Клуб Русская морская традиция» был заложен 16-ти весельный 2-ух мачтовый
                Императорский катер. Перед церемонией закладки Духовный наставник ассоциации отец Дмитрий отслужил
                молебен, киль катера был окроплен святой водой. По старой морской традиции, которую установил император
                Петр Великий, в киль катера был вложен серебряный рубль, отчеканенный в царствование Александра II. 30
                августа 2023 года катер был спущен на воду. Ходовые испытания на суровой воде Онежского озера прошли
                успешно!
            </p>
            <p>
                Катер входил во Флотилию императорских яхт и служебных судов и был построен при правлении Александра II
                в 1857 году. Катер несколько лет подряд в 1870-х годах получал звание лучшего гонщика на соревнованиях
                военно-морских флотов в г. Киль (Германия). Элегантная отделка и парусное вооружение заметно выделяли
                его среди яхт и других судов императорской Флотилии.
