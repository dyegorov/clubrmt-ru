            <h3 class="h3">
                Шестивесельный ЯЛ «Резвый» (ЯЛ-6)</h3>
            <img alt="yal_rezvy.jpg" src="/upload/medialibrary/b5b/2emr2i57v69khje7qhi8rqjt0b4df8mz.jpg"
                 title="yal_rezvy.jpg" class="floating">
            <p>
                Ял-6 – это первое судно, с которого начался флот Ассоциации.
            </p>
            <p>
                6-ти весельный ЯЛ «Резвый» – это базовое традиционное судно не только в практике нашего клуба, но и
                вообще на традиционном парусном флоте. Постижение морской науки, а именно изучение устройства корпуса,
                парусов, рангоута, бегучего и стоячего такелажа молодые моряки начинают именно с этой шлюпки. На
                шестерке же прививаются первые навыки хождения под парусом и управления кораблем. Размеры ее
                стандартные: наибольшая длина – 6,11 м, ширина – 1,85 м, высота от киля до планширя – 0,91 м, масса 650
                кг. Мачта на 6-ти весельном яле одна, называется фок-мачтой и служит опорой для парусов. Длина ее 5,5 м,
                сделана она из сосны или ели (клееная), площадь паруса 20,4 м2.
            </p>
            <p>
                Ял-6 на судах используется для связи с берегом, работ у борта (заводка швартовых, верпов и т. д.),
                перевозки лёгких грузов или людей. Также пользуется популярностью в спортивно-туристических целях.
            </p>
            <p>
            </p>

            <p>
            </p>
            <h3 class="h3">
                Четырехвесельный ЯЛ «Малый» (ЯЛ-4) </h3>
            <p>
                Эта лодка попала во флот Ассоциации из Кронштадта. Ей уже более 30 лет, но она по-прежнему в отличной
                форме, полностью укомплектована и при правильном управлении обладает отличными ходовыми качествами.
            </p>
            <p>
                По типологии это стандартный четырехвесельный ял, которым вооружались русские парусники (бриги, шхуны,
                тендеры). Размеры его стандартные: наибольшая длина – 5,26м, ширина – 1,61м, максимальная высота борта –
                1,04, максимальная осадка – 0,5м. Четверка крепилась на шлюпбалках на срезе кормы. По своему назначению,
                это малое разъездное судно, а также лодка обеспечения корабля на рейде или предназначенная для учебных
                целей.
            </p>
