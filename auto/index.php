            <h3 class="h3">
                Шестивесельный вельбот «Рюрик»</h3>
            <p>
                Вельбот – это традиционная лодка китобоев, в переводе с английского означает «китовая лодка» (англ.
                whaleboat). Это узкая весельно-парусная шлюпка с острым носом и острой кормой. Гребцы (от 4 до 8) на ней
                располагаются по одному на банке, весла на правый и левый борт идут через одно. У такой шлюпки очень
                хороший ход, на ней одинаково легко грести и табанить, ее даже можно не разворачивать – она может идти и
                задним ходом.
            </p>
            <p>
                6-ти весельный вельбот носит имя Рюрика, родоначальника княжеской (впоследствии – царской) власти.
                Вельбот «Рюрик» был построен в Петрозаводске на верфи деревянного судостроения «Варяг» в 2018 году.
                Длина корпуса 8,5м, ширина 1,9м, вес корпуса 700кг, площадь парусности 18,2м2, пассажировместимость на
                веслах 13 человек, под парусом – 8 человек. Свой первый выход вельбот совершил в акватории Истринского
                водохранилища летом 2018г.
            </p>
