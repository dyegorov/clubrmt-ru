            <h3 class="h3">
                Двухвесельный ЯЛ (ЯЛ-2) </h3>
            <p>
                Ял-2 – лодка 1977 года выпуска из Кронштадта. Приобретена на деньги участников клуба. Используется для
                обучения основам хождения под парусом школьников, интересующихся парусным спортом.
            </p>
            <h3 class="h3">
                Кроме того, для проведения спортивных мероприятий у Ассоциации имеются 6 пластиковых шлюпок «ЯЛ-6».</h3>
            <p>
                В настоящее время Ассоциация располагает четырьмя единицами ЯЛа-6, изготовленными из пластика, с
                деревянными элементами. Шлюпки, выполненные из таких материалов, ничем не отличаются от традиционных,
                деревянных, за исключением веса самой шлюпки. Размеры стандартные: наибольшая длина – 6,11 м, ширина –
                1,85 м, высота от киля до планширя – 0,91 м, масса 550 кг. Мачта на шестивесельном яле одна, называется
                фок-мачтой и служит опорой для парусов. Длина её 5,5 м, сделана она из сосны или ели (клееная), площадь
                парусного вооружения - 21,5 м2.
            </p>
            <p>
                Шлюпки изготовлены в 2020 году компанией «Корабел-Композит Архангельск», славящейся своими морскими
                кораблестроительными делами.
