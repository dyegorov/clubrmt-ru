		 Центром деятельности Ассоциации является музейная экспозиция, посвященная истории подводных сил ВМФ, расположенная на территории партнера Ассоциации – отеля M’Istra’L Hotel &amp; SPA, расположенного на Истринском водохранилище в Московский области.
