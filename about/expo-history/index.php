	<p>
		 Экспозиция была заложена 26 июля 2020 года, в День Военно-морского флота, членами Ассоциации, выпускниками-дизелистами 1992 года Ленинградского высшего военно-морского инженерного училища имени В. И. Ленина.
	</p>
	<p>
		 Узнав, что в Инкермане (Севастополь, Крым) начинается утилизация подводной лодки Б-380, последней из серии 641Б «Сом», члены Ассоциации приняли решение сохранить рубку и на ее базе сделать военно-морскую экспозицию. Началась кропотливая работа.
