		 Однако благодаря работе профессионалов-реставраторов был полностью восстановлен не только внешний облик рубки, но и реставрированы внутренние элементы: боевая рубка с верхним рубочным люком, система бортовых огней и внутреннего освещения рубки, внутренние палуба, настилы и трапы, рубочная дверь и некоторые выдвижные устройства. Венцом завершения работ стала обклейка снаружи корпуса рубки резиной, установка настоящих корабельных пакетников и розеток в боевой рубке, и конечно, покраска внутри ограждения рубки.
	</p>
	<p>
